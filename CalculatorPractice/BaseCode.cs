using System;


namespace BaseNamespace
{
    public class SimpleCalculator()
    {
        public static double SubFunc(double a, double b)
        {
            return a - b;
        }
        public static double SumFunc(double a, double b)
        {
            return a + b;
        }
        public static double MultFunc(double a, double b)
        {
            return a * b;
        }
        public static double DivFunc(double a, double b)
        {
            return a / b;
        }
    }
}